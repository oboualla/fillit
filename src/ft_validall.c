#include "fillit.h"

int	if_allvalid(char *str)
{
	int i;

	i = 0;
	if (ft_strlen(str) != 16)
		return (0);
	while (str[i])
	{
		if (str[i] != '#' && str[i] != '.')
			return (0);
		i++;
	}
	return (1);
}
